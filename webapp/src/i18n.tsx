type Translations = typeof T.ru
export type TKey = keyof Translations

const LangCtx = createContext<{ lang: Lang; setLang: (l: Lang) => void }>({
  lang: 'ru', setLang: () => {},
})

export function LanguageProvider({ children }: { children: ReactNode }) {
  const stored = (localStorage.getItem('lang') as Lang) ?? 'ru'
  const [lang, setLangState] = useState<Lang>(stored)
  const setLang = (l: Lang) => { localStorage.setItem('lang', l); setLangState(l) }
  return <LangCtx.Provider value={{ lang, setLang }}>{children}</LangCtx.Provider>
}

export function useLang() { return useContext(LangCtx) }

export function useT() {
  const { lang } = useContext(LangCtx)
  return (key: TKey) => T[lang][key] as string
}

export function usePlural() {
  const { lang } = useContext(LangCtx)
  return (n: number, forms: { ru: [string, string, string]; en: [string, string] }) => {
    if (lang === 'en') return `${n} ${n === 1 ? forms.en[0] : forms.en[1]}`
    const abs = Math.abs(n)
    const mod10 = abs % 10
    const mod100 = abs % 100
    if (mod100 >= 11 && mod100 <= 19) return `${n} ${forms.ru[2]}`
    if (mod10 === 1) return `${n} ${forms.ru[0]}`
    if (mod10 >= 2 && mod10 <= 4) return `${n} ${forms.ru[1]}`
    return `${n} ${forms.ru[2]}`
  }
}
